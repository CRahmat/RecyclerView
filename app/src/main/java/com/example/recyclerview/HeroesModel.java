package com.example.recyclerview;

public class HeroesModel {
    private String heroesName;
    private String heroesDetails;
    private int heroesImage;

    public String getHeroesName() {
        return heroesName;
    }

    public void setHeroesName(String heroesName) {
        this.heroesName = heroesName;
    }

    public String getHeroesDetails() {
        return heroesDetails;
    }

    public void setHeroesDetails(String heroesDetails) {
        this.heroesDetails = heroesDetails;
    }

    public int getHeroesImage() {
        return heroesImage;
    }

    public void setHeroesImage(int heroesImage) {
        this.heroesImage = heroesImage;
    }
}
